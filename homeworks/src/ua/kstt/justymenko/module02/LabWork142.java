public class LabWork142 {
    public static void main(String[] args) {
        int i = 10;
        int j = 3;

        int sum = i + j;
        int sub = i - j;
        int mul = i * j;
        int div = i / j;
        int mod = i % j;

        System.out.println("i + j = " + sum);
        System.out.println("i - j = " + sub);
        System.out.println("i * j = " + mul);
        System.out.println("i / j = " + div);
        System.out.println("i % j = " + mod);
    }
}
